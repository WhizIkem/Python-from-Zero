fave_fruits = ["mango", "banana", "apple", "orange"]

print(fave_fruits)

#print the first fruit in the list
print(fave_fruits[1])

# adding an item to the list
fave_fruits.append("cashew")
print(fave_fruits)

# adding an item at a specific index
fave_fruits.insert(1, "orange")
print(fave_fruits)

# deleting an item from the list
del fave_fruits[2]
print(fave_fruits)